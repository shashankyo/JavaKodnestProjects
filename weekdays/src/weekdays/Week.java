package weekdays;

public class Week {
void day(int a)
{
	switch(a) {
		case 0:
			System.out.println("Sunday");
			break;
		case 1:
			System.out.println("mon");
			break;
		case 2:
			System.out.println("tue");
			break;
		case 3:
			System.out.println("wed");
			break;
		case 4:
			System.out.println("thur");
			break;
		case 5:
			System.out.println("fri");
			break;
		case 6:
			System.out.println("sat");
			break;
		default:
			System.out.println("Enter a valid day");
}
}
}
